// 构建方式 1：纯 vite 构建，可以选择多种框架 如 vue、react等
//    npm init vite@latest
//    然后输入 y
//    项目名称 xxx
//    框架选择（vue）
//    js类型选择（ts）
//    构建成功后：npm install   npm run dev（秒启动！！！）（直接执行 vite 命令是不好使的）

// 构建方式 2：vue的方式构建，可选择vue相关的配置项较多，比如 pinia、vue-router、eslint等
//    npm init vue@latest
//    是否支持 ts
//    是否支持 jsx
//    vue-router
//    unit-test
//    pinia
//    ...
//    构建成功后：npm install  npm run dev（秒启动！！！）

